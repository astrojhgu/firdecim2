pub fn calculate_safe_shift(coeffs: &[i16]) -> u32 {
    // 1. 计算所有系数绝对值的总和 (L1 Norm)
    // 使用 i64 以防系数非常多导致 i32 溢出
    let sum_abs: i64 = coeffs.iter().map(|&c| c.abs() as i64).sum();

    // 2. 假设输入信号是满量程的 i16 (最大绝对值为 32768)
    // 理论最大累加值 = 32768 * sum_abs
    // 我们需要将这个值缩回到 15-bit (32768) 范围内。
    // 即： (32768 * sum_abs) >> bit_shift <= 32768
    // 简化后： sum_abs <= 2^bit_shift

    // 3. 计算需要的最小位移量： log2(sum_abs) 向上取整
    let safe_shift = if sum_abs <= 1 {
        0
    } else {
        // 使用 f64 计算对数并向上取整
        (sum_abs as f64).log2().ceil() as u32
    };

    println!("滤波器系数绝对值之和: {}", sum_abs);
    println!("为保证绝对不溢出，建议 bit_shift 为: {}", safe_shift);

    safe_shift
}

